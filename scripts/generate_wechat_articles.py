def create_frontmatter(article):
    tags_str = ', '.join(f'"{t}"' for t in article['tags'])
    summary = article['summary'].replace('"', '\\"')
    title = article['title'].replace('"', '\\"')
    return f"""---
title: "{title}"
summary: "{summary}"
date: "{article['date']}"
tags: [{tags_str}]
description: "{summary}"
keywords: {json.dumps(article['tags'], ensure_ascii=False)}
slug: "{article['slug']}"
---"""


def main():
    os.makedirs(OUTPUT_DIR, exist_ok=True)
    count = 0
    for article in ARTICLES:
        frontmatter = create_frontmatter(article)
        content = article['content']
        # Remove duplicate title if content starts with it
        full_md = f"{frontmatter}\n\n# {article['title']}\n\n{content}\n"
        filepath = os.path.join(OUTPUT_DIR, f"{article['slug']}.md")
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(full_md)
        count += 1
        print(f"✅ {count}. {article['title']}")

    print(f"\n总计生成 {count} 篇文章")


if __name__ == '__main__':
    main()
